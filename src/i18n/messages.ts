export function translate(
  locale: Locale,
  key: string,
  params?: Record<string, string | number>,
): string {
  let text = messages[locale][key] ?? messages.en[key] ?? key;
  if (params) {
    for (const [name, value] of Object.entries(params)) {
      text = text.replaceAll(`{${name}}`, String(value));
    }
  }
  return text;
}
